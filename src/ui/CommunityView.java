private void populateTable() {
        
        DefaultTableModel model = (DefaultTableModel) tableHouse.getModel();
        model.setRowCount(0);
        
        for(House house : community.getHouse()){
            
            Object[] data = new Object[5];
            data[0] = house.getAddress();
            data[1] = house.getCity().getCityName();
            data[2] = house.getState();
            data[3] = house.getPin();
            data[4] = house.getCommunity();
            
            model.addRow(data);
            
        }
    }
