    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int row_selected = tableHouse.getSelectedRow();

        if (row_selected < 0) {
            JOptionPane.showMessageDialog(this, "Please Select a row to delete.");
            return;
        }

        DefaultTableModel model = (DefaultTableModel) tableHouse.getModel();
        community.deleteHouse(model.getValueAt(row_selected, 4).toString());
        JOptionPane.showMessageDialog(this, "House deleted successfully");

        populateTable();

        txtAddress1.setText("");
        txtCity1.setText("");
        txtState1.setText("");
        txtZip1.setText("");
        txtCommunity1.setText("");
    }//GEN-LAST:event_jButton1ActionPerformed

    private void ViewPersonnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ViewPersonnActionPerformed
        // TODO add your handling code here:

        int row_selected = tableHouse.getSelectedRow();

        if (row_selected < 0) {
            JOptionPane.showMessageDialog(this, "Please Select a row to view details.");
            return;
        }

        DefaultTableModel model = (DefaultTableModel) tableHouse.getModel();
        House house = community.getHouses(model.getValueAt(row_selected, 4).toString());
        City city = house.getCity();

        txtAddress1.setText(house.getAddress());
        txtCity1.setText(city.getCityName());
        txtState1.setText(house.getState());
        txtZip1.setText(String.valueOf(house.getPin()));
        txtCommunity1.setText(house.getCommunity());
        
    }//GEN-LAST:event_ViewPersonnActionPerformed

