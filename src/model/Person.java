package model;

import java.util.Date;

/**
 *
 * @author swapnilbiradar
 */
public class Person {
    
    private String name;
    
    private House residence;
    private String gender;
    private String dob;
    private int id;

    public Person(String name, House residence, String gender, String dob, int id) {

        this.name = name;
        this.residence = residence;
        this.gender = gender;
        this.dob = dob;
        this.id = id;
        
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public House getResidence() {
        return residence;
    }

    public void setResidence(House residence) {
        this.residence = residence;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public String getDob() {
        return dob;
    }

    public void setDob(String dob) {
        this.dob = dob;
    }
    
    
    
}
